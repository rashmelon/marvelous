    /**
     * Handle the post "created" event.
     *
     * @param  \App\Models\Post  $post
     * @return void
     */
    public function created(Post $post)
    {
        if (! Commentary::where('category_id', $post->brand->category->id)->count()) {
            return;
        }

        $commentary = Commentary::next($post->brand->category);

        $post->commentary()->associate($commentary)->save();

        $commentary->update([
            'last_assigned' => now(),
        ]);
    }

